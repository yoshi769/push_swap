/* strdup/strndup関数は文字列を複製します．

strdup関数は，文字列sの複製である新しい文字列へのポインタを返します．

新しい文字列のためのメモリはmalloc関数で確保し，free関数で解放します．*/

#include "../../inc/libft.h"

char	*ft_strdup(const char *src)
{
	size_t	size;
	char	*dest;

	size = ft_strlen(src);
	dest = (char *)malloc(size * sizeof(char) + 1);
	if (dest == NULL)
		return (0);
	ft_memcpy(dest, src, size);
	dest[size] = '\0';
	return (dest);
}
